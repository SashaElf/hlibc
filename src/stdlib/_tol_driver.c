#include <ctype.h>
#include <stdlib.h>

char *_tol_driver(const char *s, int base, long long *ans)
{ 
	static int glph[] = { 
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	/* 0-9 start at 48 code points from zero. */
	/* v */
	'\000', '\001', '\002', '\003', '\004', '\005',
	'\006', '\007', '\010', '\011', '\000', '\000',
	/* A-Z start at 65 code points from zero. */
						/* v */
	'\000', '\000', '\000', '\000', '\000', '\012', 
	'\013', '\014', '\015', '\016', '\017', '\020', 
	'\021', '\022', '\023', '\024', '\025', '\026', 
	'\027', '\030', '\031', '\032', '\033', '\034', 
	'\035', '\036', '\037', '\040', '\041', '\042', 
	'\043', '\044', '\045', '\046', '\047', '\050', 
	'\051', '\052', '\053', '\054', '\055', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000',
	'\000', '\000', '\000', '\000', '\000', '\000' } ;

	size_t i = 0;
	size_t j = 0;
	long long ret = 0;
	long long neg = -1;

	while (isspace(s[j])) {
		++j;
	}

	switch (s[j]) {
	case '-':
		neg = 1;
	case '+':
		++j;
		break; 
	default:
		break;
	}
	switch (s[j]) {
		case '0':
			if (base == 16) { 
				++j;
				switch (s[j]) {
				case 'x':
					j += 2;
					break;
				default:
					break;
				}
			}else if (base == 0){ 
				++j;
				base = 8;
			}
		default:
			break;
	}

	for (i=j; s[i] && isdigit(s[i]); ++i) {
		ret = (base * ret) - (glph[s[i]]);
	}

	*ans = ret * neg;
	if (j)
		return s + i;
	else
		return s;
}

